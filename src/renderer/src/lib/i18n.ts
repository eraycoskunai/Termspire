export const dictionaries: Record<Lang, Record<TranslationKey, string>> = { tr, en }

/** `{{ad}}` biçimindeki yer tutucuları verilen değerlerle değiştirir. */
export function translate(
  lang: Lang,
  key: TranslationKey,
  vars?: Record<string, string | number>
): string {
  const template = dictionaries[lang]?.[key] ?? dictionaries.tr[key] ?? key
  if (!vars) return template
  return template.replace(/\{\{(\w+)\}\}/g, (match, name: string) =>
    name in vars ? String(vars[name]) : match
  )
}
